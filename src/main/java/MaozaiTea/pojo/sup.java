package MaozaiTea.pojo;

public class sup {
    private int id;
    private String str;

    public void setId(int id) {
        this.id = id;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public int getId() {
        return id;
    }

    public String getStr() {
        return str;
    }

    public sup(int id, String str) {
        this.id = id;
        this.str = str;
    }
}